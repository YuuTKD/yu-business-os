def _kw_matches(text: str, phrase: str) -> bool:
    """
    キーワードマッチ判定。スペースを含む場合はAND条件（全トークン一致）。
    """
    t = text.lower()
    tokens = phrase.split()
    return all(tok.lower() in t for tok in tokens)


def _load_from_sheet(ws) -> dict:
    """THREADS_KEYWORD_CONFIG シートからキーワードを読み込む"""
    rows = ws.get_all_records()
    result: dict = {
        CAT_OKINAWA_NOW: [],
        CAT_SEEKING: [],
        CAT_NAHA: [],
        CAT_STORE_T: [],
        CAT_STORE_H: [],
        CAT_SPECIFIC: [],
        CAT_EXCLUDE: [],
    }
    for row in rows:
        kw = str(row.get("キーワード", "")).strip()
        cat = str(row.get("スコア分類", "")).strip().upper()
        active = str(row.get("有効", "TRUE")).strip().upper()
        if not kw or active == "FALSE":
            continue
        if cat in result:
            result[cat].append(kw)
    return result


def get_keywords(ws=None, force_reload: bool = False) -> dict:
    """
    キーワード設定を取得する（キャッシュ付き）。

    Args:
        ws: gspread ワークシート（None の場合はフォールバック）
        force_reload: キャッシュを無視して再読み込み

    Returns:
        {CAT_OKINAWA_NOW: [...], CAT_SEEKING: [...], ...}
    """
    global _CACHE, _CACHE_AT

    now = time.monotonic()
    if ws is None:
        return _FALLBACK

    if not force_reload and _CACHE is not None and (now - _CACHE_AT) < _CACHE_TTL:
        return _CACHE

    try:
        loaded = _load_from_sheet(ws)
        # 空のカテゴリはフォールバックで補完
        for cat, kws in loaded.items():
            if not kws:
                loaded[cat] = _FALLBACK.get(cat, [])
        _CACHE = loaded
        _CACHE_AT = now
        logger.info(f"キーワード設定を再読み込み: 合計{sum(len(v) for v in loaded.values())}語")
        return _CACHE
    except Exception as e:
        logger.warning(f"THREADS_KEYWORD_CONFIG シート読み込み失敗、フォールバック使用: {e}")
        return _FALLBACK


def match_keywords(text: str, keywords: list[str]) -> list[str]:
    """テキストにマッチしたキーワードのリストを返す（複合フレーズ対応）"""
    return [kw for kw in keywords if _kw_matches(text, kw)]


